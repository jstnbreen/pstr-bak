		<h2>Rules</h2>

		<h4>Article I. RIDER REQUIREMENTS AND RESPONSIBILITIES</h4>
		<ul class="list-off clearfix">
			<li>
				<strong>A. Membership and Permission:</strong> All riders participating must be a member in good standing in the Pilgrim Sands Trail Riders (PSTR) and the American Motorcycle Association (AMA). If the rider is under the age of majority, he/she must bring a parent or legal guardian to Sign-In to sign a Minor Release Form.
			</li>
			<li>
				<strong>B. Protective Equipment:</strong> All riders must wear an adequate safety helmet, proper eye protection, and heavy boots.
			</li>
			<li>
				<strong>C. Entry Forms/Waiver of liability:</strong> All riders must fill out a PSTR entry form and sign a liability waiver.
			</li>
			<li>
				<strong>D. Pit Racing:</strong> Persons riding hazardously (wheelies, riding faster than a walking pace, etc.) in the parking area will be disqualified and ejected from the event.
			</li>
			<li>
				<strong>E. Leaving the Course:</strong> Riders must stay on the intended course as marked by arrows, course ribbon or fencing except when traversing double marked sections where the rider must remain between markers. A rider leaving the course can only continue in the meet by returning to the point where he left the course. A rider who has cut the course shall be disqualified.
			</li>
			<li>
				<strong>F. Unsportsmanlike Conduct: Any rider who exhibits unsportsmanlike conduct will be disqualified. This is in the sole discretion of the Trail Boss and the Event Referee.</strong>
			</li>
		</ul>

		<h4>Article II. VEHICLE REQUIREMENTS</h4>
		<ul class="list-off clearfix">
			<li>
				<strong>A. Numbers:</strong> The racing number provided by PSTR must be displayed on the front number plate of the motorcycle. If the PSTR-provided numbers are lost or destroyed, numbers of equal size and background color must displayed.
			</li>
			<li>
				<strong>B. Sound:</strong> There will be no formal sound test, but any motorcycle deemed obnoxiously loud by the Event Referee will be disqualified.
			</li>
		</ul>
