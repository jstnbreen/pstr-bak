		<p>
			Any C class rider who has finished in the top three positions two or more times in a season will be promoted to B for the following year.  So all you guys who made a career out of cleaning off the trophy shelf will have to twist it a little harder if you want to take home the hardware.
		</p>
		<ul class="list-disc">
