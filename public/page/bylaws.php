<div class="row">
	<div class="col-sm-12">
		<h1>Bylaws</h1>
		<h4 class="center uppercase bold uline">PILGRIM SANDS TRAIL RIDERS INC.</h4>
		<p class="center bold">
			Constitutional Bylaws
		</p>
		<h2>Preamble:</h2>
		<p>
			We, the members of the Pilgrim Sands Trail Riders Incorporated, in order to foster the responsible enjoyment of motorcycle trail riding and promote good relationships with the community and with other trail users, hereby establish the following Articles and Resolutions to enhance this purpose.
		</p>
		<hr>

		<h4>Article I; OFFICERS AND DUTIES</h4>
		<ol>
			<li>
				<strong class="uline">President</strong> - Overseer of committees, moderator of general meetings and senior representative of the organization.
			</li>
			<li>
				<strong class="uline">Vice President</strong> - Aide and alternate to the president as conditions demand.
			</li>
			<li>
				<strong class="uline">Treasurer</strong> - Responsible for procurement of, disbursement of, recording of, and reporting of all monetary transactions.
			</li>
			<li>
				<strong class="uline">Secretary</strong> - Responsible for the recording of minutes of meetings and official correspondence as conditions demand.
			</li>
			<li>
				<strong class="uline">President Emeritus</strong> - This is a ceremonial position with no responsibilities or officer powers. It is not mandatory that this position be filled, but if it is to be filled it will be done at the annual election. The President Emeritus is a lifetime honorary position.
			</li>
			<li>
				No member can hold two elective positions simultaneously.
			</li>
			<li>
				Additional officers may be added at a later date as per the desires of the membership.
			</li>
		</ol>
		<h4>Article II; ELECTIONS</h4>
		<ol>
			<li>
				Elections will be held yearly at the second meeting in February.
			</li>
			<li>
				Officers will be elected by a simple majority of eligible (dues paid) members present.
			</li>
			<li>
				Nominations will start no sooner than 3 months prior to elections, and will be accepted at any regular meeting prior to the election meeting.
			</li>
		</ol>
		<h4>Article III; MEMBERSHIP</h4>
		<ol>
			<li>
				Each member should also be a current member of the New England Trail Riders Association (NETRA) and/or the American Motorcycle Association (AMA).
			</li>
			<li>
				Each member should ride a motorcycle that meets current NETRA standards.
			</li>
			<li>
				Membership is open to any responsible person who meets the above requirments.
			</li>
			<li>
				Membership may be withdrawn from any member for conduct detrimental to the goals as stated in the preamble by a simple majority of eligible members at any meeting where a quorum exists. Membership may be reinstated after six months by a two thirds majority.
			</li>
		</ol>
		<h4>Article IV; MEETINGS</h4>
		<ol>
			<li>
				Regular meetings will be held on the first and third Tuesdays of each month. Meetings will involve the entire membership and will be called to order by the President, or the Vice President in his absence.
			</li>
			<li>
				A quorum will consist of the members present at any given meeting plus the President or Vice President, Due to the large amount of and the location of the membership.
			</li>
		</ol>
		<h4>Article V; FINANCIAL</h4>
		<ol>
			<li>
				The corporation will be a non-profit organization. There will be no limit to the amount of money in the treasury, but at no time will any expenditures be allowed if the balance is at or below one thousand dollars ($1,000.00) except for operation expenses of Pilgrim Sands Trail Riders Incorporated.
			</li>
			<li>
				Dues will be twenty dollars ($25.00) per year for membership (whether individual or family). Dues are to be paid by January 1st of each year.
			</li>
		</ol>
		<h4>Article VI; ACTIVITIES</h4>
		<ol>
			<li>
				The corporation will emphasize recreational type activities such as:<br>
				<strong>A:</strong> Trail Rides<br>
				<strong>B:</strong> Turkey Runs<br>
				<strong>C:</strong> Youth Activities<br>
				<strong>D:</strong> Competitive events<br>
				<strong>E:</strong> Others
			</li>
		</ol>
		<h4>Article VII; CONSTITUTIONAL REVISIONS</h4>
		<ol>
			<li>
				A two thirds majority vote, of any general meeting that has a quorum is necessary to make constitutional amendments.
			</li>
			<li>
				Constitutional amendments cannot be moved (called to a vote) during the same meeting at which the motion is made. The vote for such motions will be allowed at the next general meeting and the membership must be notified of the motion prior to said meeting.
			</li>
		</ol>
		<h4>Article VIII; RESOLUTIONS</h4>
		<ol>
			<li>
				No member can hold two (2) elective offices concurrently.
			</li>
			<li>
				Members must be present at nomination, or delegate someone to confirm his or her nomination to be placed on the ballot.
			</li>
			<li>
				Meetings are to be held at 7:00 pm.
			</li>
			<li>
				Any out of pocket expenses incurred by any member due to the conducting of official business, will be reimbursed by the Treasurer. However any questionable reimbursement will be put to a vote of the general membership for approval.
			</li>
			<li>
				The person who accepts responsibility for an event will set the rate of entry fee for said event on approval of the general membership.
			</li>
			<li>
				All organizers will make every effort to make use of different areas or locations for events.
			</li>
			<li>
				All closed course competition events will be held on 100% permissible use land if possible. All other competition events will make every effort to obtain 100% permissible use land.
			</li>
			<li>
				The executive committee consisting of the seated officers shall review the constitution annually.
			</li>
		</ol>
		<p>
			This constitution, as revised and amended, has been officially ratified by the general membership on this 5th day of February 2004.
		</p>
		<p>
			&nbsp;
		</p>
	</div>
</div>

